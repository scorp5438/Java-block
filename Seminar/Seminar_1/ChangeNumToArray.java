    public static void moveToEndArr(int[] arr) {
        int val = 7;
        int RightIndex = arr.length - 1;
        int LeftIndex = 0;

        while (arr[RightIndex] == val) {
            RightIndex--;
        }
        while (LeftIndex < RightIndex) {
            if (arr[LeftIndex] == val) {
                arr[LeftIndex] = arr[RightIndex];
                arr[RightIndex] = val;
                RightIndex--;
            }
            LeftIndex++;
        }
    }

